# LISTS
# Ordered sequences that can hold a variety of objects.
# Lists supports indexing and slicing.
# Lists can be nested and also variety of useful methods that can be called of on them.

my_list = [1, 2, 3]

my_list_2 = ['STRING', 100, 23.2]

# To get length of List
len(my_list)

# INDEXING: To get list at index 0
my_list[0]

# SLICING: To get the list after any provided index
my_list[1:]

# Concatenate lists together
new_list = my_list + my_list_2

# SOME OPERATIONS

# 1. Add an item to list : use of append
my_list.append(4)

# 2. get an item to out of list - last item : use of pop
my_list.pop()

# 3. sort items of list - last item : use of sort
my_list.sort()

# 3. reverse items of list - last item : use of reverse
my_list.reverse()


